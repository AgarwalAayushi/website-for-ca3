<p class="p2"> INTRODUCTION TO THE LOVELY PROFESSIONAL UNIVERSITY (IPS) </p>
<p class="p3">Lovely Professional University (LPU) is a private university situated in Phagwara, Punjab, India. The university was established in 2005 by Lovely International Trust, under The Lovely Professional University Act, 2005 (Punjab Act 25 of 2005) and started operation in 2006.</p><br />
<p class="p2">AFFILIATIONS</p>
<p class="p3">In India, LPU is recognised by the University Grants Commission (U), the National Council for Teacher Education (NCTE) and the Council of Architecture (COA) and approved by the Pharmacy Council of India (PCI). It is also a member of the Association of Indian Universities (AIU).

LPU's School of Agriculture is accredited by the Indian Council of Agricultural Research (ICAR). Internationally, LPU is accredited by the Accreditation Council for Business Schools and Programs (ACBSP) and a member of the Association of Commonwealth Universities (ACU). The distance education programmes are approved by the Distance Education Bureau (DEB). LPU offers overall more than 50 courses.</p><br />
